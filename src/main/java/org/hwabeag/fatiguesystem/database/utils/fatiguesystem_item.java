package org.hwabeag.fatiguesystem.database.utils;

public class fatiguesystem_item {
    private String item_data;
    private String item_name;
    private int item_point;

    public String getItemData() {
        return item_data;
    }

    public void setItemData(String ItemData) {
        this.item_data = ItemData;
    }

    public String getItemName() {
        return item_name;
    }

    public void setItemName(String ItemName) {
        this.item_name = ItemName;
    }

    public int getItemPoint() {
        return item_point;
    }

    public void setItemPoint(int ItemPoint) {
        this.item_point = ItemPoint;
    }
}
